package Inheritance;

public class Engine {

	public String engineModel;
	public String engineRPM;
	public String engineCC;
	public Engine() {
		super();
	}
	public Engine(String engineModel, String engineRPM, String engineCC) {
		super();
		this.engineModel = engineModel;
		this.engineRPM = engineRPM;
		this.engineCC = engineCC;
	}
	
	
	
}
