package Encapsulation;

public class Pen {

	private String penName;
	private String penInk;
	private String penCompany;
	
	public String getPenName() {
		return penName;
	}
	public void setPenName(String penName) {
		this.penName = penName;
	}
	public String getPenInk() {
		return penInk;
	}
	public void setPenInk(String penInk) {
		this.penInk = penInk;
	}
	public String getPenCompany() {
		return penCompany;
	}
	public void setPenCompany(String penCompany) {
		this.penCompany = penCompany;
	}
	
	
	
	
	
}
